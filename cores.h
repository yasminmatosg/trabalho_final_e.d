#ifndef CORES_H
#define CORES_H

/*
================================================
             BIBLIOTECA DE CORES
Foi utilizada para deixar estilizar o terminal.
O código de cores utilizadas segue o padrão ANSI.
================================================
*/

#define COR_VERMELHA "\x1b[0;31m"
#define COR_VERDE    "\x1b[0;32m"
#define COR_AMARELA  "\x1b[0;33m"
#define COR_AZUL     "\x1b[0;34m"
#define COR_MAGENTA  "\x1b[0;35m"
#define COR_RESET    "\x1b[0m"
#define COR_CIANO    "\x1b[0;36m"

#endif